/*
	Programa que cria uma variável do tipo caractere e imprime seu valor.
	Adicionalmente imprime uma variável inteira como uma caractere.
*/
#include <stdio.h>

int main() {
  char x = 'a';
  char y = 98;
  int z = 99;
  printf("%c\n",x);
  printf("%c\n",y);
  printf("%c\n",z);
  return 0;
}


/* SAIDA
a
b
c
*/